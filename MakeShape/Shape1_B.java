
public class Shape1_B {//structured

	public static void main(String[] args) {
		hat();
		hat();
		egg();
		stopSign();
		eggPlus();
		cup();

	}
	
	
	public static void hat(){
		System.out.println("  ______");
		System.out.println(" /      \\");
		System.out.println("/        \\");
		System.out.println("+--------+");
		System.out.println();
	}
	
	public static void egg(){
		System.out.println("  ______");
		System.out.println(" /      \\");
		System.out.println("/        \\");
		System.out.println("\\        /");		
		System.out.println(" \\______/");
		System.out.println();
	}
	
	public static void stopSign(){
		System.out.println("  ______");
		System.out.println(" /      \\");
		System.out.println("/        \\");
		System.out.println("|  STOP  |");
		System.out.println("\\        /");		
		System.out.println(" \\______/");
		System.out.println();
	}
	
	public static void eggPlus(){
		System.out.println("  ______");
		System.out.println(" /      \\");
		System.out.println("/        \\");
		System.out.println("+--------+");
		System.out.println("\\        /");		
		System.out.println(" \\______/");
		System.out.println();
	}
	
	public static void cup(){
		System.out.println("\\        /");		
		System.out.println(" \\______/");
		System.out.println("+--------+");
		System.out.println();
	}

}
